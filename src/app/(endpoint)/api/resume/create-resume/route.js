function formatResumeDate(value) {
  const date = value ? new Date(value) : null;
  if (!date || Number.isNaN(date.getTime())) return 'a previous date';

  return new Intl.DateTimeFormat('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric'
  }).format(date);
}

function isResumeOlderThanRetryThreshold(existingResume) {
  const createdAt = existingResume?.created_at ? new Date(existingResume.created_at) : null;
  if (!createdAt || Number.isNaN(createdAt.getTime())) return false;
  return Date.now() - createdAt.getTime() >= DUPLICATE_HISTORY_STALE_DAYS * 86400000;
}

