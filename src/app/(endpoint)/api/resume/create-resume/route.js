function buildDuplicateResumeMessage({ existingResume, currentUser, existingResumeOwner, similarityScore = 1 }) {
  const createdLabel = formatResumeDate(existingResume?.created_at);
  const isStale = isResumeOlderThanRetryThreshold(existingResume);
  const isSameUser = String(existingResume?.associatedUserId || '') === String(currentUser?._id || '');
  const ownerName = existingResumeOwner?.username?.trim() || 'another VA';

  // Similarity note is appended as a clean separate sentence at the end of
  // each message variant rather than injected mid-sentence after a date label.
  const similarityNote =
    similarityScore < 1
      ? ` A similar job description (${Math.round(similarityScore * 100)}% match) was previously used.`
      : '';

  if (isStale) {
    if (isSameUser) {
      return `You already generated this resume on ${createdLabel}. It looks older, so please review the history before generating again.${similarityNote}`;
    }
    return `This profile and job description were already used by ${ownerName} on ${createdLabel}. Please review the history before generating again.${similarityNote}`;
  }

  if (isSameUser) {
    return `You have already generated this resume on ${createdLabel}. Please visit the history.${similarityNote}`;
  }

  return `This resume was already generated by ${ownerName} on ${createdLabel}. Please visit the history.${similarityNote}`;
}

